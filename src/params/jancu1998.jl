# ── Parse and merge ──
const JANCU1998 = let
    d1 = parse_table(JANCU1998_IV_RAW)
    d2 = parse_table(JANCU1998_III_V_RAW)
    merge(d1, d2)
end

# ── Accessors ──
get_params(::Type{SP3D5Sstar}, ::Jancu1998, material::String) = JANCU1998[material]

function list_materials(::Jancu1998)
    for (name, d) in sort(collect(JANCU1998))
        c, a = _cation_anion(name)
        type_str = c == a ? "diamond" : "$c-$a"
        println("  $name  ($type_str, a=$(d[:a]) Å)")
    end
end
